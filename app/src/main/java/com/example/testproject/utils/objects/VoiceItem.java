package com.example.testproject.utils.objects;

public class VoiceItem {

    private int icon;
    private String title;

    public VoiceItem(int icon, String title) {
        this.icon = icon;
        this.title = title;
    }

    public int getIcon() {
        return icon;
    }

    public String getTitle() {
        return title;
    }
}
